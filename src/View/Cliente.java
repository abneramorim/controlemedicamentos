    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        try {
            //System.out.println("Cliente: " + toJsonCliente());
            CtrlCliente CTRLCli = new CtrlCliente();
            if (toJsonCliente() == null) {
                return;
            }
            int codigoCli = CTRLCli.Salvar(toJsonCliente());
            txtCodigo.setText(String.valueOf(codigoCli));
            Mensagem.information("Cliente cadastrado com sucesso", this);
            //atualizaTela();
            controlaAcesso(true);
        } catch (Exception ex) {
            if (ex.getMessage().contains("Duplicate entry")) {
                Mensagem.error("J√° existe um cliente com a carteira informada !!!", this);
                txtCartao.selectAll();
            } else {
                Mensagem.error("N√£o foi possivel cadastrar o cliente", this);
            }
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

