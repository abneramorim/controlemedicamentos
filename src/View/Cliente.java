    private void txtCartaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCartaoKeyPressed
        if(!txtCartao.isEditable()){
            return;
        }
        
        if((evt.getKeyCode() >= 33 && evt.getKeyCode() <= 40) || evt.getKeyCode() == 127 || evt.getKeyCode() == 8 || evt.getKeyCode() == 10)
                return;
        
        /*if (evt.getKeyCode() == 10) {
        btnSalvarActionPerformed(new java.awt.event.ActionEvent(this, WIDTH, "Salvar"));
        }*/
        
        if (!Character.isDigit(evt.getKeyChar())) {
            String texto = txtCartao.getText();
            int tamanho = texto.length();
            txtCartao.setText(texto.substring(0, tamanho));
            Mensagem.warning("Informe apenas n√∫meros!!!", this);//System.out.println("Digito");
            return;
        }
    }//GEN-LAST:event_txtCartaoKeyPressed

