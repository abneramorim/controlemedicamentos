    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            ControllerBaseCtrl ctrlCli = new CtrlCliente();
            int codigoCli = Integer.parseInt(txtCodigo.getText());
            String json;
            //limpaTelaCompleta();
            if (codigoCli > 0) { //Pesquisa direta
                json = ctrlCli.Recuperar(codigoCli);

                if (json.isEmpty()) {
                    if (Mensagem.confirm("Cliente n√£o encontrado\nDeseja abrir a tela de pesquisa?", this)) {
                        Pesquisa pesq = new Pesquisa(new javax.swing.JFrame(), true, "CLIENTE", new String());
                        pesq.setVisible(true);
                        //System.out.println("Identificador retornado: " + pesq.getResultPesq());
                        setJsonTelaCliente(new CtrlCliente().RecuperarCliCompCartao(pesq.getResultPesq()));
                    }
                    return;
                } else {
                    setJsonTelaCliente(json);
                    controlaAcesso(true);
                }
            } else { //Abre a tela de pesquisa
                setJsonTelaCliente(new CtrlCliente().RecuperaUltimoRegistro());
            }
        } catch (NumberFormatException ex) {
            txtCodigo.requestFocus();

        } catch (Exception ex) {
            Mensagem.warning(ex.getMessage(), this);
        } finally {
            txtCodigo.selectAll();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

