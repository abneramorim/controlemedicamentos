    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(!Mensagem.confirm("Deseja excluir este cliente?", this)){
            return;
        }
        int codigo = txtCodigo.getText().isEmpty() ? 0 : Integer.parseInt(txtCodigo.getText());
        if (codigo > 0) {
            objCtrlCli.Deletar(codigo);
            Mensagem.information("Cliente excluido com sucesso", this);
            limpaTelaCompleta();
            setJsonTelaCliente(new CtrlCliente().RecuperaRegistroAnterior(codigo));
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(!Mensagem.confirm("Deseja descartar as altera√ß√µes?", this)){
            return;
        }
        controlaAcesso(true);
        CtrlCliente CTRLCli = new CtrlCliente();
        if (!isAlterar) {
            setJsonTelaCliente(CTRLCli.Recuperar(codBackup));
        } else {
            setJsonTelaCliente(CTRLCli.Recuperar(Integer.parseInt(txtCodigo.getText())));
        }
        codBackup = -1;
    }//GEN-LAST:event_btnCancelarActionPerformed

