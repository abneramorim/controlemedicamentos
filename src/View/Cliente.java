    public String toJsonCliente() throws Exception {
        objJsonObject = new JsonObject();

        objJsonObject.add("nome_cli", new JsonPrimitive(txtNome.getText()));
        if (txtCartao.getText().length() == 22) {
            objJsonObject.add("cartao_cli", new JsonPrimitive(txtCartao.getText()));
        } else {
            Mensagem.error("Cart√£o Incoreto", this);
            return null;
        }
        if (isAlterar) {
            objJsonObject.add("objCod_objBase", new JsonPrimitive(Integer.parseInt(txtCodigo.getText())));
        }

        //System.out.println(new Gson().toJson(objJsonObject));
        return new Gson().toJson(objJsonObject);
    }

    public void setJsonTelaCliente(String json) {
        if (json == null || json.isEmpty()) {
            return;
        }

        objJsonObject = objParser.parse(json).getAsJsonObject();
        txtNome.setText(objJsonObject.get("nome_cli").getAsString());
        txtCodigo.setText(objJsonObject.get("objCod_objBase").getAsString());
        txtCartao.setText(objJsonObject.get("cartao_cli").getAsString());
    }

