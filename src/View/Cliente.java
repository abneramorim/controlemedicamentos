    private void atualizaTela() {
        ControllerBaseCtrl ctrlCli = new CtrlCliente();
        int codigoCli = Integer.parseInt(txtCodigo.getText());
        String json;
        json = ctrlCli.Recuperar(codigoCli);
        limpaTelaCompleta();
        if (!json.isEmpty()) {
            setJsonTelaCliente(json);
        }
    }

