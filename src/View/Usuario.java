    private void btnBuscarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCodigoActionPerformed

        try {
            if (txtCodigo.getText().isEmpty()) {
                return;
            }

            String json = new CtrlUsuario().Recuperar(Integer.parseInt(txtCodigo.getText()));

            if (json.isEmpty()) {
                //throw new Exception("Produto n√£o encontrado");
                if (Mensagem.confirm("Usuario n√£o encontrado\nDeseja abrir a tela de pesquisa?", this)) {
                    Pesquisa pesq = new Pesquisa(new javax.swing.JFrame(), true, "USUARIO", new String());
                    pesq.setVisible(true);
                    //System.out.println("Identificador retornado: " + pesq.getResultPesq());
                    txtCodigo.setText(String.valueOf(pesq.getCodigo()));
                    btnBuscarCodigoActionPerformed(new java.awt.event.ActionEvent(this, 0, "Preencher"));
                    controlaAcesso(true);
                }
                return;
            } else {
                jsonToTela(json);
                controlaAcesso(true);
            }

        } catch (Exception e) {
            limpaTela();
            Mensagem.error("Codigo invalido ou Usuario nao encontrado ", this);
            jsonToTela(new CtrlUsuario().RecuperaUltimoRegistro());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarCodigoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (!Mensagem.confirm("Deseja excluir este usuario?", this)) {
            return;
        }
        try {
            new CtrlUsuario().Deletar(Integer.parseInt(txtCodigo.getText()));
            limpaTela();
            jsonToTela(new CtrlUsuario().RecuperaRegistroAnterior(Integer.parseInt(txtCodigo.getText())));
        } catch (Exception e) {
            Mensagem.error("Codigo invalido ou Usuario nao encontrado ", this);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

