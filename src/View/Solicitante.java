    private void txtCRMFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCRMFocusLost
        String texto = txtCRM.getText();
        if (texto == null || texto.isEmpty()) {
            return;
        }

        int size = texto.length();
        for (int i = 0; i < size; i++) {
            if (!Character.isDigit(texto.charAt(i)) && texto.charAt(i) != '.' && texto.charAt(i) != ',') {
                txtCRM.requestFocus();
                txtCRM.selectAll();
                Mensagem.warning("Informe apenas nÃºmeros!!!", this);//System.out.println("Digito");
                return;
            }
        }
    }//GEN-LAST:event_txtCRMFocusLost

