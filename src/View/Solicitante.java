    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            CtrlSolicitante ctrlSol = new CtrlSolicitante();
            int codigoCli = Integer.parseInt(txtCodigo.getText());
            String json;
            //limpaTelaCompleta();
            if (codigoCli > 0) { //Pesquisa direta
                json = ctrlSol.Recuperar(codigoCli);

                if (json.isEmpty()) {
                    if (Mensagem.confirm("Solicitante nÃ£o encontrado\nDeseja abrir a tela de pesquisa?", this)) {
                        Pesquisa pesq = new Pesquisa(new javax.swing.JFrame(), true, "SOLICITANTE", new String());
                        pesq.setVisible(true);
                        //System.out.println("Identificador retornado: " + pesq.getResultPesq());
                        setJsonTelaSolicitante(ctrlSol.RecuperarSolCRM(pesq.getResultPesq()));
                    }
                } else {
                    setJsonTelaSolicitante(json);
                    controlaAcesso(true);
                }
            } else { //Abre a tela de pesquisa
                setJsonTelaSolicitante(new CtrlSolicitante().RecuperaUltimoRegistro());
            }
        } catch (NumberFormatException ex) {
            txtCodigo.requestFocus();

        } catch (Exception ex) {
            Mensagem.warning(ex.getMessage(), this);
        } finally {
            txtCodigo.selectAll();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

