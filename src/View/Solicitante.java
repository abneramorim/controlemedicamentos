    public String toJsonSolicitante() throws Exception {
        objJsonObject = new JsonObject();

        objJsonObject.add("nome_sol", new JsonPrimitive(txtNome.getText()));
        objJsonObject.add("crm_sol", new JsonPrimitive(txtCRM.getText()));

        if (isAlterar) {
            objJsonObject.add("objCod_objBase", new JsonPrimitive(Integer.parseInt(txtCodigo.getText())));
        }

        //System.out.println(new Gson().toJson(objJsonObject));
        return new Gson().toJson(objJsonObject);
    }

    public void setJsonTelaSolicitante(String json) {
        if (json == null || json.isEmpty()) {
            return;
        }

        objJsonObject = objParser.parse(json).getAsJsonObject();
        txtNome.setText(objJsonObject.get("nome_sol").getAsString());
        txtCodigo.setText(objJsonObject.get("objCod_objBase").getAsString());
        txtCRM.setText(objJsonObject.get("crm_sol").getAsString());
    }

