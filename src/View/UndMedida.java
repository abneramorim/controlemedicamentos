    private void limpaTela() {
        txtCodigo.setText("");
        txtUnidade.setText("");
    }

    private void controlaAcesso(boolean status) {
        btnIncluir.setEnabled(status);
        btnSalvar.setEnabled(!status);
        btnExcluir.setEnabled(status);
        btnAlterar.setEnabled(status);
        btnCancelar.setEnabled(!status);
        txtUnidade.setEditable(!status);
        btnPrimeiroUsuario.setEnabled(status);
        btnAnterior.setEnabled(status);
        btnProximo.setEnabled(status);
        btnUltimo.setEnabled(status);
        txtCodigo.setEnabled(status);
    }

    private String telaToJson() {
        JsonObject json = new JsonObject();

        if (isAlterar) {
            json.addProperty("objCod_objBase", txtCodigo.getText());
        }
        json.addProperty("unidade", txtUnidade.getText());

        return json.toString();
    }

    private void jsonToTela(String json) {
        if (json == null || json.isEmpty()) {
            return;
        }

        JsonObject objJson = new JsonParser().parse(json).getAsJsonObject();
        //System.out.println("json: " + json);
        txtUnidade.setText(objJson.get("unidade").getAsString());
        txtCodigo.setText(objJson.get("objCod_objBase").getAsString());
    }

