    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        //System.out.println("Codigo: " + new JsonParser().parse(new CtrlProduto().RecuperaPrimeiroRegistro()).getAsJsonObject().get("objCod_objBase").getAsString().equals(txtCodigo.getText()));
        if(txtCodigo.getText().isEmpty()){
            return;
        }
        if (txtCodigo.getText().equals(new JsonParser().parse(new CtrlUndMedida().RecuperaPrimeiroRegistro()).getAsJsonObject().get("objCod_objBase").getAsString())) {
            Mensagem.information("Esta é a primeira unidade de medida !!!", this);
            return;
        }

        String json = new CtrlUndMedida().RecuperaRegistroAnterior(Integer.parseInt(txtCodigo.getText()));

        if (json != null) {
            jsonToTela(json);
        }
    }//GEN-LAST:event_btnAnteriorActionPerformed

