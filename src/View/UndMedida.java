    private void btnBuscarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCodigoActionPerformed
        if(txtCodigo.getText().isEmpty()){
            return;
        }
        try {
            
            int cod = Integer.parseInt(txtCodigo.getText());
            JsonObject objJson = new JsonParser().parse(new CtrlUndMedida().Recuperar(cod)).getAsJsonObject();

            txtCodigo.setText(objJson.get("objCod_objBase").getAsString());
            txtUnidade.setText(objJson.get("unidade").getAsString());
            
            controlaAcesso(true);
        } catch (Exception e) {
            limpaTela();
            Mensagem.error("Codigo invalido ou Unidade de Medida não encontrada ", this);
            jsonToTela(new CtrlUndMedida().RecuperaUltimoRegistro());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarCodigoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(txtCodigo.getText().isEmpty()){
            return;
        }
        if(!Mensagem.confirm("Deseja excluir esta unidade de medida?", this)){
            return;
        }
        try {
            new CtrlUndMedida().Deletar(Integer.parseInt(txtCodigo.getText()));
            limpaTela();
            jsonToTela(new CtrlUndMedida().RecuperaRegistroAnterior(Integer.parseInt(txtCodigo.getText())));
        } catch (Exception e) {
            Mensagem.error("Codigo invalido ou unidade de medida não encontrada ", this);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtUnidade.getText().isEmpty()) {
            Mensagem.error("Preencha todos os campos para continuar !!!", this);
            return;
        }

        try {
            int codigo = new CtrlUndMedida().Salvar(telaToJson());
            txtCodigo.setText(String.valueOf(codigo));
            //System.out.println("Terminou !!!!!!!!!!!!!!!!!!!!!!!");
            //limpaTela();
            controlaAcesso(true);
        } catch (Exception e) {
            //limpaTela();
            Mensagem.error("Nao foi possível salvar a unidade de medida", this);
            if (e.getMessage().contains("for key 'unidade'")) {
                Mensagem.error("Erro ao salvar\nJá existe uma unidade de medida com mesmo nome!!!", this);
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(!Mensagem.confirm("Deseja descartar as alterações?", this)){
            return;
        }
        controlaAcesso(true);
        limpaTela();
        if (codigoBKP <= 0) {
            jsonToTela(new CtrlUndMedida().RecuperaUltimoRegistro());
        }
        jsonToTela(new CtrlUndMedida().Recuperar(codigoBKP));
    }//GEN-LAST:event_btnCancelarActionPerformed

