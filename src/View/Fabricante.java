    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            ControllerBaseCtrl ctrlFab = new CtrlFabricante();
            int codigoCli = Integer.parseInt(txtCodigo.getText());
            String json;
            //limpaTelaCompleta();
            if (codigoCli > 0) { //Pesquisa direta
                json = ctrlFab.Recuperar(codigoCli);

                if (json.isEmpty()) {
                    if (Mensagem.confirm("Fabricante não encontrado\nDeseja abrir a tela de pesquisa?", this)) {
                        Pesquisa pesq = new Pesquisa(new javax.swing.JFrame(), true, "FABRICANTE", new String());
                        pesq.setVisible(true);
                        //System.out.println("Identificador retornado: " + pesq.getResultPesq());
                        setJsonTelaFabricante(new CtrlFabricante().RecuperarFabCNPJ(pesq.getResultPesq()));
                    }
                } else {
                    setJsonTelaFabricante(json);
                    controlaAcesso(true);
                }
            } else { //Abre a tela de pesquisa

            }
        } catch (NumberFormatException ex) {
            txtCodigo.requestFocus();

        } catch (Exception ex) {
            Mensagem.warning(ex.getMessage(), this);
        } finally {
            txtCodigo.selectAll();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

