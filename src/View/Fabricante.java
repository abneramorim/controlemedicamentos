    private void txtCNPJKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCNPJKeyPressed
        if (!txtCNPJ.isEditable()) {
            return;
        }

        if ((evt.getKeyCode() >= 33 && evt.getKeyCode() <= 40) || evt.getKeyCode() == 127 || evt.getKeyCode() == 8 || evt.getKeyCode() == 10) {
            return;
        }

        /*if (evt.getKeyCode() == 10) {
        btnSalvarActionPerformed(new java.awt.event.ActionEvent(this, 0, "Salvar"));
        }*/
        if (!Character.isDigit(evt.getKeyChar())) {
            String texto = txtCNPJ.getText();
            int tamanho = texto.length();
            txtCNPJ.setText(texto.substring(0, tamanho));
            Mensagem.warning("Informe apenas números!!!", this);//System.out.println("Digito");
            return;
        }

    }//GEN-LAST:event_txtCNPJKeyPressed

