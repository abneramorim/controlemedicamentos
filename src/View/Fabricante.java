    private void atualizaTela() {
        ControllerBaseCtrl ctrlSol = new CtrlSolicitante();
        int codigoCli = Integer.parseInt(txtCodigo.getText());
        String json;
        json = ctrlSol.Recuperar(codigoCli);
        limpaTelaCompleta();
        if (!json.isEmpty()) {
            setJsonTelaFabricante(json);
        }
    }

