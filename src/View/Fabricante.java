    public String toJsonFabricante() throws Exception {
        objJsonObject = new JsonObject();

        objJsonObject.add("nome_fab", new JsonPrimitive(txtNomeFab.getText()));
        objJsonObject.add("cnpj_fab", new JsonPrimitive(txtCNPJ.getText()));

        if (isAlterar) {
            objJsonObject.add("objCod_objBase", new JsonPrimitive(Integer.parseInt(txtCodigo.getText())));
        }

        //System.out.println(new Gson().toJson(objJsonObject));
        return new Gson().toJson(objJsonObject);
    }

    public void setJsonTelaFabricante(String json) {
        if (json == null || json.isEmpty()) {
            return;
        }

        objJsonObject = objParser.parse(json).getAsJsonObject();
        txtNomeFab.setText(objJsonObject.get("nome_fab").getAsString());
        txtCodigo.setText(objJsonObject.get("objCod_objBase").getAsString());
        txtCNPJ.setText(objJsonObject.get("cnpj_fab").getAsString());
    }

