    private String dateFormatBanco(String pDate) {
        Date data = new Date();
        data.setTime(Date.parse(pDate));
        //System.out.println("data: " + data.toString());
        SimpleDateFormat formatador = new SimpleDateFormat("yyyy-dd-MM");
        //System.out.println("data: " + formatador.format(data));
        return formatador.format(data);
    }

    private String dateFormatBanco(Date pDate) {
        SimpleDateFormat formatador = new SimpleDateFormat("yyyy-dd-MM");
        return formatador.format(pDate);
    }
    
    private Date dateFormatDate(String pDate) {
        Date data = new Date();
        data.setTime(Date.parse(pDate.split("/")[1] + "/" + pDate.split("/")[0] + "/" + pDate.split("/")[2]));
        //System.out.println("Data: " + data.toString());
        return data;
    }

