    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String dataInicio = txtDataInicio.getText();
        String dataFim = txtDataFim.getText();
        String produto = txtProduto.getText().split(" ")[0];
        String solicitante = txtSolicitante.getText().split(" ")[0];
        String lote = txtLote.getText().split(" ")[0];
        String responsavel = txtResponsavel.getText().split(" ")[0];
        String cliente = txtCliente.getText();
        String cuidados = txtCuidados.getText();
        
        if (dataInicio.isEmpty() && dataFim.isEmpty()
                && produto.isEmpty() && solicitante.isEmpty()
                && lote.isEmpty() && responsavel.isEmpty()
                && cliente.isEmpty() && cuidados.isEmpty()) {
            Mensagem.error("Informe algum campo para realizar a busca", this);
        }
        
        if(jckEmTodaParte.isSelected() && !cuidados.isEmpty()){
            cuidados = "%" + cuidados + "%";
        }
        //boolean isPreenchido = false;
        SimpleDateFormat formatador = new SimpleDateFormat("yyyy-MM-dd");
        if (dataInicio.equals("  /  /    ") ^ dataFim.equals("  /  /    ")) {
            if (!dataInicio.equals("  /  /    ")) {
                Date dataInicioDate = dateFormatDate(dataInicio);
                dataInicio = formatador.format(dataInicioDate);
                dataFim = formatador.format(new Date());
            } else {
                Date umAnoAtras = new Date();
                umAnoAtras.setDate(new Date().getDate() - 366);
                dataInicio = formatador.format(umAnoAtras);
                dataFim = formatador.format(dateFormatDate(dataFim));
            }
        } else if (!dataInicio.equals("  /  /    ") && !dataFim.equals("  /  /    ")) {
            Date dataInicioDate = dateFormatDate(dataInicio);
            Date dataFimDate = dateFormatDate(dataFim);
            dataInicio = formatador.format(dataInicioDate);
            dataFim = formatador.format(dataFimDate);
        } else if (dataInicio.equals("  /  /    ") && dataFim.equals("  /  /    ")) {
            dataInicio = "";
            dataFim = "";
        }
        //System.out.println("dateFormatBanco (dataFim): " + dataFim + " - (dataInicio): " + dataInicio);
        List<String[]> pedidos = new CtrlPedido().SelecionarPorCampo_List(dataInicio, dataFim, produto, solicitante, lote, responsavel, cliente, cuidados);
        int size = pedidos.size();
        ModeloTab.setNumRows(0);
        if(size == 0){
            Mensagem.information("Não foram encontrados resultados\nPara os parâmetros informados !!", this);
            jlNumResult.setText("0 Resultado Encontrado");
            return;
        }
        
        for (int i = 0; i < size; i++) {
            //System.out.println(pedidos.get(i)[1].split("-")[2]+"/"+pedidos.get(i)[1].split("-")[1]+"/"+pedidos.get(i)[1].split("-")[0] + " data_normal: " + pedidos.get(i)[1]);
            ModeloTab.addRow(new String[]{pedidos.get(i)[0], pedidos.get(i)[1].split("-")[2]+"/"+pedidos.get(i)[1].split("-")[1]+"/"+pedidos.get(i)[1].split("-")[0], pedidos.get(i)[2], pedidos.get(i)[3], pedidos.get(i)[4], pedidos.get(i)[5]});
        }
        if(size == 1)
            jlNumResult.setText("1 Resultado Encontrado");
        else
            jlNumResult.setText(size + " Resultados Encontrados");
            
    }//GEN-LAST:event_btnBuscarActionPerformed

